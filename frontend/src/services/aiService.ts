  analysis_type?: 'week' | 'tomorrow' | 'general'; // –î–æ–±–∞–≤–ª—è–µ–º –ø–æ–¥–¥–µ—Ä–∂–∫—É —Ç–∏–ø–æ–≤ –∞–Ω–∞–ª–∏–∑–∞
