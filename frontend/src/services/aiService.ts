class AIService {
  private readonly AI_CACHE_TTL = 24 * 60 * 60 * 1000; // 24 —á–∞—Å–∞ –≤ –º–∏–ª–ª–∏—Å–µ–∫—É–Ω–¥–∞—Ö

  /**
   * –ê–Ω–∞–ª–∏–∑ –∫–∞–ª–µ–Ω–¥–∞—Ä—è —Å –ø–æ–º–æ—â—å—é –ò–ò (–ø—Ä–æ—Å—Ç–æ–π –∞—Å–∏–Ω—Ö—Ä–æ–Ω–Ω—ã–π –ø–æ–¥—Ö–æ–¥)
   */
  async analyzeCalendar(
    requestData: CalendarAnalysisRequest,
    forceRefresh: boolean = false
  ): Promise<CalendarAnalysis> {
    try {
      console.log('üîç Starting analyzeCalendar with data:', {
        analysis_type: requestData.analysis_type,
        events_count: requestData.calendar_events?.length,
        goals_count: requestData.user_goals?.length,
        analysis_period_days: requestData.analysis_period_days,
        forceRefresh
      });

      // –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–µ—à
      if (!forceRefresh) {
        const cachedResult = cacheService.getByData<CalendarAnalysis>(requestData);
        if (cachedResult) {
          console.log('üìã Using cached AI analysis');
          return cachedResult;
        }
      }

      console.log('ü§ñ Requesting AI analysis to:', '/ai/analyze-calendar');
      console.log('üì§ Request payload:', JSON.stringify(requestData, null, 2));

      const response = await api.post('/ai/analyze-calendar', requestData);

      console.log('‚úÖ AI analysis response received:', response.status);
      console.log('üì• Response data:', response.data);

      // –ö–µ—à–∏—Ä—É–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
      cacheService.setByData(requestData, response.data, this.AI_CACHE_TTL);

      return response.data;
    } catch (error: any) {
      console.error('‚ùå Error in calendar analysis:', error);
      console.error('Error details:', {
        message: error.message,
        status: error.response?.status,
        statusText: error.response?.statusText,
        data: error.response?.data
      });
      throw this.handleAPIError(error, '–û—à–∏–±–∫–∞ –ø—Ä–∏ –∞–Ω–∞–ª–∏–∑–µ –∫–∞–ª–µ–Ω–¥–∞—Ä—è');
    }
  }

  /**
   * –ü–ª–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—è –¥–ª—è —Ü–µ–ª–∏
   */
  async planGoal(requestData: any): Promise<any> {
    try {
      console.log('üéØ Requesting goal planning...');
      const response = await api.post('/ai/plan-goal', requestData);
      return response.data;
    } catch (error: any) {
      console.error('Error planning goal:', error);
      throw this.handleAPIError(error, '–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–ª–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏–∏ —Ü–µ–ª–∏');
    }
  }

  /**
   * –û–±—Ä–∞–±–æ—Ç–∫–∞ –æ—à–∏–±–æ–∫ API
   */
  private handleAPIError(error: any, defaultMessage: string): Error {
    if (error.response?.data?.detail) {
      if (Array.isArray(error.response.data.detail)) {
        // –ú–∞—Å—Å–∏–≤ –æ—à–∏–±–æ–∫ –≤–∞–ª–∏–¥–∞—Ü–∏–∏
        const validationErrors = error.response.data.detail
          .map((err: any) => `${err.loc?.join('.')} - ${err.msg}`)
          .join('; ');
        return new Error(`–û—à–∏–±–∫–∏ –≤–∞–ª–∏–¥–∞—Ü–∏–∏: ${validationErrors}`);
      } else if (typeof error.response.data.detail === 'string') {
        // –°—Ç—Ä–æ–∫–æ–≤–∞—è –æ—à–∏–±–∫–∞
        return new Error(error.response.data.detail);
      }
    }
    return new Error(defaultMessage);
  }

  /**
   * –û—á–∏—Å—Ç–∫–∞ –∫–µ—à–∞ –ò–ò
   */
  clearAICache(): void {
    cacheService.clearAICache();
  }

  /**
   * –ü–æ–ª—É—á–µ–Ω–∏–µ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –æ –∫–µ—à–µ –ò–ò
   */
  getCacheInfo() {
    return cacheService.getCacheInfo();
  }

  /**
   * –ü–æ–ª—É—á–µ–Ω–∏–µ —Ü–µ–ª–µ–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
   */
  async getGoals(includeCompleted: boolean = false): Promise<SmartGoal[]> {
    try {
      const response = await api.get(`/ai/goals?include_completed=${includeCompleted}`);
      return response.data;
    } catch (error) {
      console.error('Error getting user goals:', error);
      throw new Error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ —Ü–µ–ª–µ–π');
    }
  }

  /**
   * –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å–æ–±—ã—Ç–∏—è –∫–∞–ª–µ–Ω–¥–∞—Ä—è
   */
  async updateCalendarEvent(eventId: string, updateData: any): Promise<any> {
    try {
      // Backend –∏—Å–ø–æ–ª—å–∑—É–µ—Ç PATCH /calendar/event/{event_id}
      const response = await api.patch(`/calendar/event/${eventId}`, updateData);
      return response.data;
    } catch (error) {
      console.error('Error updating calendar event:', error);
      throw new Error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ —Å–æ–±—ã—Ç–∏—è');
    }
  }

  /**
   * –ü—Ä–∏–º–µ–Ω–µ–Ω–∏–µ –∏–∑–º–µ–Ω–µ–Ω–∏—è –≤ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏–∏
   */
  async applyScheduleChange(change: ScheduleChange): Promise<boolean> {
    try {
      const response = await api.post('/ai/apply-schedule-change', change);
      return response.data.success;
    } catch (error) {
      console.error('Error applying schedule change:', error);
      throw new Error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø—Ä–∏–º–µ–Ω–µ–Ω–∏–∏ –∏–∑–º–µ–Ω–µ–Ω–∏—è');
    }
  }

  /**
   * –û–±—â–µ–Ω–∏–µ —Å –ò–ò –≤ —á–∞—Ç-—Ñ–æ—Ä–º–∞—Ç–µ
   */
  async chatWithAI(
    messages: Array<{role: string; content: string}>,
    systemPrompt?: string
  ): Promise<string> {
    try {
      const response = await api.post('/ai/chat', {
        messages: messages,
        system_prompt: systemPrompt,
        model: 'gpt-4',
        temperature: 0.7,
        max_tokens: 1000
      });

      return response.data.content;
    } catch (error) {
      console.error('Error in AI chat:', error);
      throw new Error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±—â–µ–Ω–∏–∏ —Å –ò–ò');
    }
  }

  /**
   * –°–æ–∑–¥–∞–Ω–∏–µ SMART —Ü–µ–ª–∏
   */
  async createSMARTGoal(goal: SmartGoal): Promise<SmartGoal> {
    try {
      const response = await api.post('/ai/goals', goal);
      return response.data;
    } catch (error) {
      console.error('Error creating SMART goal:', error);
      throw new Error('–û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ —Ü–µ–ª–∏');
    }
  }

  /**
   * –ê–Ω–∞–ª–∏–∑ —Ü–µ–ª–∏ —Å –ø–æ–º–æ—â—å—é –ò–ò
   */
  async analyzeGoal(goal: SmartGoal): Promise<GoalAnalysis> {
    try {
      const response = await api.post('/ai/analyze-goal', goal);
      return response.data;
    } catch (error) {
      console.error('Error analyzing goal:', error);
      throw new Error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –∞–Ω–∞–ª–∏–∑–µ —Ü–µ–ª–∏');
    }
  }

  /**
   * –°–æ–∑–¥–∞–Ω–∏–µ –ø–æ–ª–Ω–æ–≥–æ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—è –Ω–∞ –¥–µ–Ω—å –∏–ª–∏ –Ω–µ–¥–µ–ª—é
   */
  async createFullSchedule(requestData: FullScheduleRequest): Promise<FullScheduleResponse> {
    try {
      console.log('üìÖ Creating full schedule with data:', {
        schedule_type: requestData.schedule_type,
        goals_count: requestData.user_goals?.length,
        existing_events_count: requestData.existing_events?.length
      });

      const response = await api.post('/ai/create-full-schedule', requestData);
      
      console.log('‚úÖ Full schedule created:', response.data);
      return response.data;
    } catch (error: any) {
      console.error('‚ùå Error creating full schedule:', error);
      throw this.handleAPIError(error, '–û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—è');
    }
  }

  /**
   * –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å—É—â–µ—Å—Ç–≤—É—é—â–µ–π —Ü–µ–ª–∏
   */
  async updateGoal(goalId: string, goalData: SmartGoal): Promise<SmartGoal> {
    try {
      const response = await api.put(`/ai/goals/${goalId}`, goalData);
      return response.data;
    } catch (error: any) {
      console.error('Error updating goal:', error);
      throw this.handleAPIError(error, '–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ —Ü–µ–ª–∏');
    }
  }

  /**
   * –£–¥–∞–ª–µ–Ω–∏–µ —Ü–µ–ª–∏
   */
  async deleteGoal(goalId: string): Promise<void> {
    try {
      await api.delete(`/ai/goals/${goalId}`);
    } catch (error: any) {
      console.error('Error deleting goal:', error);
      throw this.handleAPIError(error, '–û—à–∏–±–∫–∞ –ø—Ä–∏ —É–¥–∞–ª–µ–Ω–∏–∏ —Ü–µ–ª–∏');
    }
  }

  /**
   * –°–æ–∑–¥–∞–Ω–∏–µ –Ω–æ–≤–æ–≥–æ —Å–æ–±—ã—Ç–∏—è –≤ –∫–∞–ª–µ–Ω–¥–∞—Ä–µ
   */
  async createCalendarEvent(eventData: {
    summary: string;
    description?: string;
    start: {
      dateTime: string;
      timeZone?: string;
    };
    end: {
      dateTime: string;
      timeZone?: string;
    };
    location?: string;
    recurrence?: string[];
  }): Promise<any> {
    try {
      console.log('üìÖ Creating calendar event:', eventData);
      const response = await api.post('/calendar/events', eventData);
      console.log('‚úÖ Calendar event created:', response.data);
      return response.data;
    } catch (error: any) {
      console.error('‚ùå Error creating calendar event:', error);
      throw this.handleAPIError(error, '–û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ —Å–æ–±—ã—Ç–∏—è');
    }
  }

  /**
   * –û—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏ –ò–ò (—É–¥–∞–ª–µ–Ω–∏–µ –∏–∑ –∫–µ—à–∞)
   */
  async rejectScheduleChange(changeId: string, analysisType: 'week' | 'tomorrow' | 'general'): Promise<void> {
    try {
      // –ó–¥–µ—Å—å –º–æ–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å –ª–æ–≥–∏–∫—É –¥–ª—è —É–¥–∞–ª–µ–Ω–∏—è –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–π —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏ –∏–∑ –∫–µ—à–∞
      // –ü–æ–∫–∞ –ø—Ä–æ—Å—Ç–æ –ª–æ–≥–∏—Ä—É–µ–º –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ
      console.log(`‚ùå Rejected schedule change: ${changeId} for analysis type: ${analysisType}`);

      // –ú–æ–∂–Ω–æ –æ—á–∏—Å—Ç–∏—Ç—å –≤–µ—Å—å –∫–µ—à –¥–ª—è –¥–∞–Ω–Ω–æ–≥–æ —Ç–∏–ø–∞ –∞–Ω–∞–ª–∏–∑–∞
      // –∏–ª–∏ —Ä–µ–∞–ª–∏–∑–æ–≤–∞—Ç—å –±–æ–ª–µ–µ —Ç–æ—á–µ—á–Ω–æ–µ —É–¥–∞–ª–µ–Ω–∏–µ
      this.clearAICache();
    } catch (error: any) {
      console.error('Error rejecting schedule change:', error);
      throw new Error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–∏ —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏');
    }
  }
}

