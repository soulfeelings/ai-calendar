  recurrence?: RecurrenceSuggestion; // –ù–ï–û–ë–Ø–ó–ê–¢–ï–õ–¨–ù–û: –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ –ø–æ –ø–æ–≤—Ç–æ—Ä—è–µ–º–æ—Å—Ç–∏
  // –ù–æ–≤—ã–µ –ø–æ–ª—è –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è —Å–æ–±—ã—Ç–∏–π
  description?: string;
  location?: string;
